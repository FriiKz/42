#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;

	i = 0;
	if (n > 0)
	{
		while ((unsigned char *)&s[i] && (i < n))
		{
			*(unsigned char *)&s[i] = 0;
			i++;
		}
		n--;
	}
}
