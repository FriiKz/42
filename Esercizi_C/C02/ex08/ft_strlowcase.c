#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	char	*t;

	t = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str + 32;
		str++;
	}
	return (t);
}
